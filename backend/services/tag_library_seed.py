async def seed_tag_library():
    """应用启动时自动填充标签库种子数据，已存在的不会重复插入"""
    async with async_session() as session:
        count = 0
        for dimension, tags in SEED_DATA.items():
            for sort_idx, (name, desc) in enumerate(tags):
                result = await session.execute(
                    select(TagLibrary).where(
                        TagLibrary.name == name,
                        TagLibrary.dimension == dimension,
                    )
                )
                if result.scalar_one_or_none():
                    continue

                session.add(TagLibrary(
                    name=name,
                    dimension=dimension,
                    description=desc,
                    sort_order=sort_idx,
                ))
                count += 1

        await session.commit()
        if count > 0:
            logger.info(f"标签库种子数据: 新增 {count} 条")
        else:
            logger.info("标签库种子数据: 已是最新，无需更新")
